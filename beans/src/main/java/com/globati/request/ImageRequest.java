package com.globati.request;

public class ImageRequest {
    String id;
    String path;

    public ImageRequest() {
    }

    public ImageRequest(String id, String path) {
        this.id = id;
        this.path = path;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
